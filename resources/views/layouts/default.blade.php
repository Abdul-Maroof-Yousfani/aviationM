	function toWords(id) {

		s = $('#d_t_amount_'+id+'').val();
		s= parseFloat(s).toFixed(1);

		s = s.toString();
		s = s.replace(/[\, ]/g,'');
		if (s != parseFloat(s)) return 'not a number';
		var x = s.indexOf('.');
		if (x == -1)
			x = s.length;
		if (x > 15)
			return 'too big';
		var n = s.split('');
		var str = '';
		var sk = 0;
		for (var i=0;   i < x;  i++) {
			if ((x-i)%3==2) {
				if (n[i] == '1') {
					str += tn[Number(n[i+1])] + ' ';
					i++;
					sk=1;
				} else if (n[i]!=0) {
					str += tw[n[i]-2] + ' ';
					sk=1;
				}
			} else if (n[i]!=0) { // 0235
				str += dg[n[i]] +' ';
				if ((x-i)%3==0) str += 'hundred ';
				sk=1;
			}
			if ((x-i)%3==1) {
				if (sk)
					str += th[(x-i-1)/3] + ' ';
				sk=0;
			}
		}

		if (x != s.length) {
			var y = s.length;
			// str += 'point ';
			str += 'cents ';
			for (var i=x+1; i<y; i++)
				str += dg[n[i]] +' ';
		}
		result = str.replace(/\s+/g,' ')+'Only';

		$('#rupees').text('Amount In Words:'+' '+result);
		$('#rupees'+id).text('Amount In Words:'+' '+result);
		$('#rupees').val(result);
		$('#rupeess'+id).val(result);

		var currency=  $('#curren :selected').text();
		currency=currency.split('-');
		var text=$('#rupees').text();
		text=text +' '+'(' + currency[0] + ' ) ';

		$('#rupees').text(text);


	};


